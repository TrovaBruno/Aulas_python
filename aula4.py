"""
Tipos int e float
int = numero inteiro
positivo ou negativo 

"""
print(11) #int
print(-11) #int
print(0) #int

"""
Float = Numero com ponto flutuante
Representa qualquer número
float sem sinal é considerado positivo
"""

print(1.1, 10.11)
print(0.0)

"""
A função type mostra o tipo que o Python inferiu 
"""

print(type('Otavio'))
print(type(1))
print(type(1.01))